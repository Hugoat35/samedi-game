function uniqValidLen(words: readonly string[], len: number): string[] {
  const seen = new Set<string>();
  const out: string[] = [];
  for (const w of words) {
    const u = w.toUpperCase();
    if (u.length !== len || !/^[A-Z]+$/.test(u)) continue;
    if (seen.has(u)) continue;
    seen.add(u);
    out.push(u);
  }
  out.sort();
  return out;
}

export const WORDS_FR_5: readonly string[] = uniqValidLen(RAW, 5);
export const WORDS_FR_6: readonly string[] = uniqValidLen(RAW6, 6);
export const WORDS_FR_7: readonly string[] = uniqValidLen(RAW7, 7);

/** Longueur du mot selon le nombre de joueurs (aligné sur `wordle_start_game` en base). */
export function wordLengthForPlayerCount(n: number): 5 | 6 | 7 {
  if (n <= 3) return 5;
  if (n <= 5) return 6;
  return 7;
}

export function isValidWordFr(w: string, len: number): boolean {
  const u = w.trim().toUpperCase();
  if (u.length !== len || !/^[A-Z]+$/.test(u)) return false;
  const list = len === 5 ? WORDS_FR_5 : len === 6 ? WORDS_FR_6 : len === 7 ? WORDS_FR_7 : [];
  return list.includes(u);
}

export function isValidWordFr5(w: string): boolean {
  return isValidWordFr(w, 5);
}
